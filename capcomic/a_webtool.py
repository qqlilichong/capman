import t_ptree
import a_httpmr

#######################################################################################################

def mainbus(cfgfile):
    ini = t_ptree.PTini()
    ini.read(cfgfile)
    a_httpmr.farming({beanid: ini[beanid].copy() for beanid in ini.smatch(r'(bean.\w+)').keys()})

#######################################################################################################

if __name__ == '__main__':
    mainbus(r'configer_beans.ini')

